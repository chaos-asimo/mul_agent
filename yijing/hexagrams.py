def get_hexagram_by_index(row: int, col: int) -> dict:
    cell = LIU_SHI_SI_GUA_MATRIX[row][col]
    parts = cell.split()
    number = int(parts[0])
    name = parts[1]
    detail = LIU_SHI_SI_GUA_DETAILS.get(name, {})
    return {
        "number": number,
        "name": name,
        "full_name": detail.get("name", name),
        "description": detail.get("description", ""),
        "image": detail.get("image", ""),
        "row": row,
        "col": col
    }

def get_hexagram_by_name(name: str) -> dict:
    return LIU_SHI_SI_GUA_DETAILS.get(name, {})

def get_yao_text(hexagram_name: str, position: int) -> str:
    if hexagram_name in YAO_TEXTS and position in YAO_TEXTS[hexagram_name]:
        return YAO_TEXTS[hexagram_name][position]
    return ""

def get_extra_text(hexagram_name: str) -> str:
    if hexagram_name in YAO_TEXTS and "extra" in YAO_TEXTS[hexagram_name]:
        return YAO_TEXTS[hexagram_name]["extra"]
    return ""
